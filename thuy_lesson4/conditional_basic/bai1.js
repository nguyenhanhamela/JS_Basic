/** 1. Cho 2 số: num_1 = 10, num_2 = 5.
 * Sử dụng câu lệnh rẽ nhanh if/else để log ra màn hình số lớn nhất trong 2 số trên. */

let num_1 = 10;
let num_2 = 5;

if (num_1 > num_2) {
  console.log(`So lon nhat la: ${num_1}`);
} else if (num_1 < num_2) {
  console.log(`So lon nhat la: ${num_2}`);
} else {
  console.log(`Hai so bang nhau!`);
}
